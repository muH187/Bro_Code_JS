function showDetails(name, city, country, age) {
    console.log(`My name is ${name}`)
    console.log(`I'm from ${city} ${country}`)
    console.log(`I'm ${age} years old`)
}

showDetails("Muhammad Ali", "Karachi", "Pakistan", "19")
showDetails("Muhammad Mirza", "Islamabad", "Pakistan", "19")

function add(x, y) {
    return x + y
}

console.log(add(2, 5))

function subtract(y, x) {
    return x - y
}

console.log(subtract(10, 5))